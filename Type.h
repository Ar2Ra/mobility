typedef unsigned char      uint8;
typedef short              int16;
typedef unsigned short     uint16;
typedef int                int32;
typedef unsigned int       uint32;
typedef long long          int64;
typedef unsigned long long uint64;
typedef unsigned char      BIT;
typedef unsigned int       BOOL;

#endif  /* __TYPE_H__ */
